package ru.voskhod;

public class Request {
    private String name;
    private String value;

    String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
